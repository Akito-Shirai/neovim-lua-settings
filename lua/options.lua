--------------------------------------------------------------------------------
-- 1. 言語設定
--------------------------------------------------------------------------------
-- vim.cmd("language ja_JP.UTF-8")
vim.cmd("language en_US.UTF-8")

--------------------------------------------------------------------------------
-- 2. 基本エディタ設定
--------------------------------------------------------------------------------
local options = {
  encoding     = "utf-8",      -- エンコーディング
  fileencoding = "utf-8",      -- ファイルのエンコーディング
  swapfile     = false,        -- スワップファイルを作成しない
  backup       = false,        -- バックアップファイルを作成しない
  backupskip   = { "/tmp/*", "/private/tmp/*" },
  writebackup  = false,        -- 書き込み前のバックアップを作成しない
  undofile     = true,         -- アンドゥファイルを作成
  clipboard    = "unnamedplus",-- レジスタとクリップボードを共有
}

--------------------------------------------------------------------------------
-- 3. UI設定
--------------------------------------------------------------------------------
-- 表示やステータスライン、色などの外観に関する設定
options.title         = true          -- ウィンドウタイトルを表示
options.cmdheight     = 2            -- コマンドラインの高さ
options.laststatus    = 3            -- ステータスラインの表示モード
options.showmode      = true        -- モード表示を隠す
options.signcolumn    = "yes"        -- シンカラムを常に表示
options.number        = true         -- 行番号を表示
options.relativenumber= false        -- 相対行番号を使用
options.numberwidth   = 4            -- 行番号の幅
options.wrap          = false        -- 行の折り返しをしない
options.showtabline   = 2            -- タブラインの表示
options.cursorline    = true         -- カーソル行をハイライト
options.scrolloff     = 8            -- 縦スクロール時の余白
options.sidescrolloff = 8            -- 横スクロール時の余白
options.background    = "dark"       -- 背景色を暗色に設定
options.termguicolors = true         -- True Color対応
options.guifont       = "monospace:h17"
options.winblend      = 0            -- ウィンドウ透明度

--------------------------------------------------------------------------------
-- 4. 検索設定
--------------------------------------------------------------------------------
options.hlsearch  = true   -- 検索結果をハイライト
-- options.incsearch = true -- インクリメンタルサーチ
options.ignorecase= true   -- 大文字小文字の違いを無視して検索
options.smartcase = true   -- 大文字を含む場合のみ大文字小文字を区別

--------------------------------------------------------------------------------
-- 5. インデント設定
--------------------------------------------------------------------------------
options.shiftwidth  = 2    -- シフト幅
options.tabstop     = 2    -- タブ幅
options.expandtab   = true -- タブをスペースに変換
options.smartindent = true -- インデントをスマートに調整
-- options.autoindent = true -- 自動インデント

--------------------------------------------------------------------------------
-- 6. 補完・コマンドライン関連
--------------------------------------------------------------------------------
-- コマンドライン補完やポップアップメニューなどの設定
options.completeopt  = { "menuone", "noselect" }
options.conceallevel = 0
options.pumheight    = 10  -- ポップアップメニューの高さ
-- options.wildmenu   = true  -- コマンドライン補完（コメントアウト例）
-- options.showcmd    = true  -- 入力中のコマンドを表示
options.wildoptions  = "pum"
options.pumblend     = 5

--------------------------------------------------------------------------------
-- 7. ウィンドウ分割
--------------------------------------------------------------------------------
options.splitbelow = false -- 横分割時に下に新ウィンドウを開くかどうか
options.splitright = false -- 縦分割時に右に新ウィンドウを開くかどうか

--------------------------------------------------------------------------------
-- 8. その他の動作
--------------------------------------------------------------------------------
options.mouse      = "niv"   -- マウス操作を有効化
options.updatetime = 300   -- CursorHold発動までの時間
options.timeoutlen = 300   -- キーマッピング待ち時間

--------------------------------------------------------------------------------
-- 追加設定 (shortmess, formatoptionsなど)
--------------------------------------------------------------------------------
vim.opt.shortmess:append("c")
vim.opt.formatoptions:remove({"c", "r", "o"})

--------------------------------------------------------------------------------
-- テーブルに定義した値を vim.opt へ反映
--------------------------------------------------------------------------------
for k, v in pairs(options) do
  vim.opt[k] = v
end

--------------------------------------------------------------------------------
-- 行移動の折り返し設定・単語区切り
--------------------------------------------------------------------------------
vim.cmd("set whichwrap+=<,>,[,],h,l")  -- 行頭/行末を超えた移動を可能にする
vim.cmd([[set iskeyword+=-]])         -- 単語区切りにハイフンを含める






